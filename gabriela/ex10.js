//Criar um programa com duas variáveis e mostrar o resultado das quatro operações matemáticas
let num1 = 10
let num2 = 5
let adição = num1 + num2
let diminuição = num1 - num2
let mutiplicação = num1 * num2
let divisão = num1 / num2 
